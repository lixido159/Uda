package com.example.m.calendertwo.widget;

public class LunarInfo {
    private int mMonth;
    private int mDay;

    public int getMonth() {
        return mMonth;
    }

    public int getDay() {
        return mDay;
    }

    public LunarInfo(int mMonth, int mDay) {

        this.mMonth = mMonth;
        this.mDay = mDay;
    }
}
